#!/usr/bin/env bash
set -euo pipefail
# в”Ђв”Ђ uninstall.sh в”Ђв”Ђ Development setup uninstaller в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

RED=$'\033[0;31m'
GREEN=$'\033[0;32m'
CYAN=$'\033[0;36m'
BOLD=$'\033[1m'
NC=$'\033[0m'

lower() { echo "$1" | tr '[:upper:]' '[:lower:]'; }

INSTALL_DIR="${HOME}/.local/share/ai-commit"
BIN="${HOME}/.local/bin/ai-commit"

echo ""
echo -e "${BOLD}${CYAN}  Development Setup Uninstaller${NC}"
echo ""

# в”Ђв”Ђ Remove files в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

[ -d "$INSTALL_DIR" ] && rm -rf "$INSTALL_DIR" && echo -e "${GREEN}  Removed ${INSTALL_DIR}${NC}"
[ -f "$BIN" ]         && rm -f "$BIN"          && echo -e "${GREEN}  Removed ${BIN}${NC}"

# в”Ђв”Ђ Remove Ollama model в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

echo ""
read -r -p "  Also delete Ollama 'buun-commit' model(s)? [y/N] " answer
if [[ "$(lower "$answer")" == "y" ]]; then
  for model in $(ollama list 2>/dev/null | grep -o 'buun-commit[^ ]*' || true); do
    ollama rm "$model" 2>/dev/null && echo -e "${GREEN}  Removed model: ${model}${NC}"
  done
fi

# в”Ђв”Ђ Done в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

echo ""
echo -e "${GREEN}${BOLD}  вњ“ Uninstalled.${NC}"
echo ""
